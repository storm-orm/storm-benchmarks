CREATE TABLE city (
    id   BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    name VARCHAR(100) NOT NULL
);

-- The tables the write workloads insert into (owner and pet for the graph
-- insert, visit for the insert workloads) feed their primary keys from
-- explicit sequences rather than identity columns so that every library can
-- use JDBC batching: Hibernate's recommended setup allocates ids client-side
-- from the sequence (allocationSize 50, hence INCREMENT BY 50), while the
-- other libraries rely on the column default. Seed rows use explicit ids far
-- below the sequence start, so benchmark-inserted rows are recognizable by
-- id >= 1000000 and can be deleted between iterations.
CREATE SEQUENCE owner_seq START WITH 1000000 INCREMENT BY 50;

CREATE TABLE owner (
    id         BIGINT PRIMARY KEY DEFAULT nextval('owner_seq'),
    first_name VARCHAR(50) NOT NULL,
    last_name  VARCHAR(50) NOT NULL,
    address    VARCHAR(120) NOT NULL,
    telephone  VARCHAR(20) NOT NULL,
    city_id    BIGINT NOT NULL REFERENCES city (id)
);

CREATE TABLE pet_type (
    id   BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    name VARCHAR(50) NOT NULL
);

CREATE SEQUENCE pet_seq START WITH 1000000 INCREMENT BY 50;

CREATE TABLE pet (
    id         BIGINT PRIMARY KEY DEFAULT nextval('pet_seq'),
    name       VARCHAR(50) NOT NULL,
    birth_date DATE NOT NULL,
    type_id    BIGINT NOT NULL REFERENCES pet_type (id),
    owner_id   BIGINT NOT NULL REFERENCES owner (id)
);

CREATE SEQUENCE visit_seq START WITH 1000000 INCREMENT BY 50;

CREATE TABLE visit (
    id          BIGINT PRIMARY KEY DEFAULT nextval('visit_seq'),
    pet_id      BIGINT NOT NULL REFERENCES pet (id),
    visit_date  DATE NOT NULL,
    description VARCHAR(200) NOT NULL
);

CREATE INDEX idx_owner_city ON owner (city_id);
CREATE INDEX idx_pet_owner ON pet (owner_id);
CREATE INDEX idx_pet_type ON pet (type_id);
CREATE INDEX idx_visit_pet ON visit (pet_id);
